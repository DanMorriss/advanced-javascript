// Summing an array of numbers:
const nums = [0, 1, 2, 3, 4];
let sum = nums.reduce((acc, curr) => acc + curr, 0); // ,0 is the initial item to start with, it's optional and if left out the fist item in the array will be used. Even though it's the same as the first value, it's good practice to include it.
console.log(sum);

